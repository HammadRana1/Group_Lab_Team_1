    private void loadAdminProfile() {
        if (userAccount == null) {
            JOptionPane.showMessageDialog(this, "No admin user loaded!");
            return;
        }

        // Get the associated person object
        Person p = userAccount.getAssociatedPersonProfile().getPerson();

        // Populate fields
        IDField.setText(p.getPersonId());
        NameField.setText(p.getName());
        EmailField.setText(p.getEmail());
        DepartmentField.setText(p.getDepartment());
        ContactField.setText(p.getContact());
        CBStatus.setSelectedItem(p.getAcademicStatus());

        // Make ID non-editable
        IDField.setEditable(false);
    }

