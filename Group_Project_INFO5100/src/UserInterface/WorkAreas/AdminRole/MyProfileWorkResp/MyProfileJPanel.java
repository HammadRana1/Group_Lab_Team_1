    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (userAccount == null) {
            JOptionPane.showMessageDialog(this, "No admin user found!");
            return;
        }

        Person p = userAccount.getAssociatedPersonProfile().getPerson();

        // Update fields
        p.setName(NameField.getText().trim());
        p.setEmail(EmailField.getText().trim());
        p.setDepartment(DepartmentField.getText().trim());
        p.setContact(ContactField.getText().trim());
        p.setAcademicStatus(CBStatus.getSelectedItem().toString());

        JOptionPane.showMessageDialog(this, "Profile updated successfully!");
    }//GEN-LAST:event_btnSaveActionPerformed

